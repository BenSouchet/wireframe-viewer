#include "libft.h"

int		dark_color(int clr, int r, int g, int b)
{
	r = ((clr >> 16) & 0xFF) - (((clr >> 16) & 0xFF) * 0.35);
	g = ((clr >> 8) & 0xFF) - (((clr >> 8) & 0xFF) * 0.35);
	b = ((clr) & 0xFF) - (((clr) & 0xFF) * 0.35);
	return (((r & 0xff) << 16) + ((g & 0xff) << 8) + (b & 0xff));
}
